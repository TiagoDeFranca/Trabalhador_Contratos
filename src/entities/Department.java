package entities;

public class Department {
	private String name;
	
	
	//constructors
	public Department() {
	
	}
	public Department(String name) {

		this.name = name;
	}
	
	
	//get and set
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
